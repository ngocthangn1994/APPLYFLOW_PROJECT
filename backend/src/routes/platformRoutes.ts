/**
 * GET /chat/threads/:id/messages
 */
router.get(
  "/chat/threads/:id/messages",
  requireAuth,
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);
    const { id } = objectIdParamSchema.parse(req.params);

    const thread = await ChatThread.findById(id);
    if (!thread) throw notFound("Chat thread not found");

    const isParticipant =
      Array.isArray((thread as any).participantIds) &&
      (thread as any).participantIds.some(
        (participantId: any) => String(participantId) === String(user.id),
      );

    if (!isParticipant && user.role !== "admin") {
      throw forbidden("You do not have access to this thread");
    }

    const messages = await ChatMessage.find({ threadId: id }).sort({
      createdAt: 1,
    });

    ok(res, messages);
  }),
);

/**
 * POST /chat/threads/:id/messages
 */
router.post(
  "/chat/threads/:id/messages",
  requireAuth,
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);
    const { id } = objectIdParamSchema.parse(req.params);
    const body = chatMessageSchema.parse(req.body);

    const thread = await ChatThread.findById(id);
    if (!thread) throw notFound("Chat thread not found");

    const isParticipant =
      Array.isArray((thread as any).participantIds) &&
      (thread as any).participantIds.some(
        (participantId: any) => String(participantId) === String(user.id),
      );

    if (!isParticipant && user.role !== "admin") {
      throw forbidden("You do not have access to send messages in this thread");
    }

    const message = await ChatMessage.create({
      threadId: id,
      senderId: user.id,
      body: body.body,
      attachments: body.attachments,
    });

    await ChatThread.findByIdAndUpdate(id, {
      $set: { updatedAt: new Date() },
    });

    created(res, message);
  }),
);

