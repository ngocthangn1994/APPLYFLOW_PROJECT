/**
 * GET /assistant/dashboard
 */
router.get(
  "/assistant/dashboard",
  requireAuth,
  requireRole("assistant", "admin"),
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);

    const [delegatedCount, dueToday, blockedCount, recentDelegated] =
      await Promise.all([
        DelegatedJob.countDocuments({
          assistantId: user.id,
          status: { $in: ["new", "in_progress", "needs_client_input"] },
        }),
        Task.countDocuments({
          assistantId: user.id,
          dueAt: { $lte: new Date() },
          status: { $ne: "done" },
        }),
        DelegatedJob.countDocuments({
          assistantId: user.id,
          status: "blocked",
        }),
        DelegatedJob.find({ assistantId: user.id })
          .sort({ updatedAt: -1 })
          .limit(5)
          .populate("jobId")
          .populate("clientId", "fullName email"),
      ]);

    ok(res, {
      delegatedCount,
      dueToday,
      blockedCount,
      recentDelegated,
    });
  }),
);

