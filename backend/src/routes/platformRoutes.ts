const router = Router();

const asyncHandler =
  (fn: (req: Request, res: Response, next: NextFunction) => Promise<void>) =>
  (req: Request, res: Response, next: NextFunction) => {
    Promise.resolve(fn(req, res, next)).catch(next);
  };

const objectIdParamSchema = z.object({
  id: z.string().min(1, "Missing id param"),
});

const saveJobSchema = z.object({
  status: z.enum(["saved", "archived"]).optional().default("saved"),
});

const delegateJobSchema = z.object({
  assistantId: z.string().min(1, "assistantId is required"),
  notes: z.string().max(2000).optional().default(""),
});

const chatMessageSchema = z.object({
  body: z.string().trim().min(1, "Message body is required").max(5000),
  attachments: z.array(z.any()).optional().default([]),
});

const subscribeSchema = z.object({
  planId: z.string().min(1, "planId is required"),
});

const applicationAnswersSchema = z.object({
  answers: z.record(z.any()),
});

const preferencesSchema = z.object({
  emailUpdates: z.boolean().optional(),
  whatsappUpdates: z.boolean().optional(),
  dailyProgressReport: z.boolean().optional(),
  escalationAlerts: z.boolean().optional(),
  autoGenerateDocuments: z.boolean().optional(),
  assistantNotifications: z.boolean().optional(),
  preferredLocations: z.array(z.string()).optional(),
  remotePreference: z.enum(["remote", "hybrid", "onsite", "any"]).optional(),
  salaryMin: z.number().optional(),
  salaryMax: z.number().optional(),
  targetTitles: z.array(z.string()).optional(),
});

function getPagination(req: Request) {
  const page = Math.max(Number(req.query.page) || 1, 1);
  const limit = Math.min(Math.max(Number(req.query.limit) || 20, 1), 100);
  const skip = (page - 1) * limit;
  return { page, limit, skip };
}

function ok(res: Response, data: unknown, meta?: Record<string, unknown>) {
  return res.json({
    success: true,
    data,
    ...(meta ? { meta } : {}),
  });
}

function created(res: Response, data: unknown) {
  return res.status(201).json({
    success: true,
    data,
  });
}

function ensureUser(req: Request) {
  if (!req.user) {
    const err = new Error("Unauthorized");
    (err as any).statusCode = 401;
    throw err;
  }
  return req.user;
}

function badRequest(message: string) {
  const err = new Error(message);
  (err as any).statusCode = 400;
  return err;
}

function forbidden(message = "Forbidden") {
  const err = new Error(message);
  (err as any).statusCode = 403;
  return err;
}

function notFound(message = "Resource not found") {
  const err = new Error(message);
  (err as any).statusCode = 404;
  return err;
}

/**
 * GET /jobs
 * Public to authenticated users
 * Supports search + filters + pagination
 */
router.get(
  "/jobs",
  requireAuth,
  asyncHandler(async (req, res) => {
    const { page, limit, skip } = getPagination(req);
    const search = String(req.query.search || "").trim();
    const location = String(req.query.location || "").trim();
    const remoteType = String(req.query.remoteType || "").trim();

    const query: Record<string, any> = {};

    if (search) {
      query.$or = [
        { title: { $regex: search, $options: "i" } },
        { company: { $regex: search, $options: "i" } },
        { skills: { $elemMatch: { $regex: search, $options: "i" } } },
      ];
    }

    if (location) {
      query.location = { $regex: location, $options: "i" };
    }

    if (remoteType) {
      query.remoteType = remoteType;
    }

    const [items, total] = await Promise.all([
      Job.find(query)
        .sort({ postedAt: -1, createdAt: -1 })
        .skip(skip)
        .limit(limit),
      Job.countDocuments(query),
    ]);

    ok(res, items, {
      pagination: {
        page,
        limit,
        total,
        totalPages: Math.ceil(total / limit),
      },
    });
  }),
);

/**
 * POST /jobs/:id/save
 */
router.post(
  "/jobs/:id/save",
  requireAuth,
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);
    const { id } = objectIdParamSchema.parse(req.params);
    const body = saveJobSchema.parse(req.body || {});

    const job = await Job.findById(id);
    if (!job) throw notFound("Job not found");

    const saved = await SavedJob.findOneAndUpdate(
      { clientId: user.id, jobId: id },
      {
        $set: {
          clientId: user.id,
          jobId: id,
          status: body.status,
        },
      },
      { upsert: true, new: true, setDefaultsOnInsert: true },
    );

    ok(res, saved);
  }),
);

/**
 * POST /jobs/:id/delegate
 */
router.post(
  "/jobs/:id/delegate",
  requireAuth,
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);
    const { id } = objectIdParamSchema.parse(req.params);
    const body = delegateJobSchema.parse(req.body);

    const [job, assistant] = await Promise.all([
      Job.findById(id),
      User.findById(body.assistantId),
    ]);

    if (!job) throw notFound("Job not found");
    if (!assistant) throw notFound("Assistant not found");

    if (assistant.role !== "assistant" && assistant.role !== "admin") {
      throw badRequest("Selected user is not an assistant");
    }

    const delegated = await DelegatedJob.create({
      clientId: user.id,
      assistantId: body.assistantId,
      jobId: id,
      status: "new",
      notes: body.notes,
    });

    created(res, delegated);
  }),
);

/**
 * GET /inbox
 * Only fetch this user's threads + messages
 */
router.get(
  "/inbox",
  requireAuth,
  asyncHandler(async (req, res) => {
    const user = ensureUser(req);

    const threads = await EmailThread.find({ userId: user.id }).sort({
      updatedAt: -1,
    });

    const threadIds = threads.map((thread: any) => thread._id);

    const messages = await EmailMessage.find({
      threadId: { $in: threadIds },
    })
      .sort({ createdAt: -1 })
      .limit(50);

    ok(res, {
      threads,
      messages,
    });
  }),
);

